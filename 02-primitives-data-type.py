#  Variables with primitive data types
my_integer = 1
my_float = 3.14
my_complex_number = 1 + 2j
my_string = "Hello, Coder!"
my_list = [1, 2, 3]
my_tuple = (4.0, 5.5, 6.9)
my_dictionary = {"key": "value"}
my_set = {7, 8, 9}
my_boolean = False
my_none = None

#  The 'type' function returns the data type.
print(type(my_integer))         #  Integer
print(type(my_float))           #  Float       
print(type(my_complex_number))  #  Complex
print(type(my_string))          #  String
print(type(my_list))            #  List
print(type(my_tuple))           #  Tuple
print(type(my_dictionary))      #  Dictionary
print(type(my_set))             #  Set
print(type(my_boolean))         #  Boolean
print(type(my_none))            #  NoneType
